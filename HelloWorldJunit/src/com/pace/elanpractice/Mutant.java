package com.pace.elanpractice;

public class Mutant 
{
	/*
	 * private string name
	 * setName()
	 * getName()
	 */
	
	private String name;
	
	public Mutant() {
		name = null;
	}
	
	public void setName(String inName) {
		name = inName;
	}
	
	public String getName() {
		return name;
	}
}

